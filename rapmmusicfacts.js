const data = [
    'Before he became a rapper, Tupac started his career as a roadie and a dancer with Digital Underground',
    'Ice-T was the originally rapper of 99 Problems, from his album Home Invasions in 1993, but Jay-Z made it famous.',
    'Eminem\'s first album "Infinite" from 1996 ony sold 1000 copies.',
    'Lauryn Hill is the only female rapper to win a full five Grammys.',
    'Fetty Wap\'s disfigured left eye is the result of an injury from his childhood.',
    'Kendrick Lamar used to work as a security guard before he became a rapper.',
    'Dr. Dre\s album, "The Chronic 2001" is commonly just called "The Chronic", but it is actually a throwback to his 1992 album actually just titled "The Chronic".',
    'In the 1970s, "hip hop" began to develop in the South Bronx in New York City as an underground urban movement.',
    
];

